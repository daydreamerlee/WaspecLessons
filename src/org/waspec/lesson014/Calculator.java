public class Calculator {
    public int add (int x, int y){
        return x+y;
    }

    public int minus(int x, int y){
        int result;
        result=x-y;
        return result;
    }

    public double minus(double x,double y){
        double result;
        result=x-y;
        return result;
    }

    public int minus(int x, int y, int z){
        return x-y-z;
    }

    public double minus(double a, double b, double c){
        return a-b-c;
    }
}
